print("Calculates the area of trapezium")

while True:
  s1 = float(input("Side 1? "))
  s2 = float(input("Side 2? "))
  h = float(input("Height? "))
  
  print("Area:")
  print((s1 + s2) / 2 * h)
  print(" ")
  # sweet and simple. more programs should be like this
  
